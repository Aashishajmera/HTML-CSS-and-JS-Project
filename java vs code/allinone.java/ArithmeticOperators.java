public class ArithmeticOperators {

    public static void main(String[] args) {

        int a = 20, b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        // ese bhi kr sakte hai

        int c;

        c = a + b;

        System.out.println(c);
    }

}
