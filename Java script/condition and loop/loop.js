// loop in java script language

// for loop

console.log("This is for loop: ");
for (let i = 0; i <= 5; i++) {
  console.log(i);
}

// while loop

console.log("its while loop: ");
let i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

// do while loop

console.log("do while loop: ");
let j = 0;
do {
  console.log(j);
  j++;
} while (j > 5);
